import uvicorn
import argparse
import os

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Run the FastAPI server")
    parser.add_argument(
        "--port", type=int, required=True, help="Port number to run the server on"
    )
    parser.add_argument(
        "--reload", type=str, default="false", help="Reload the server on code changes"
    )
    args = parser.parse_args()
    reload_mode = args.reload.lower() == "true"
    
    # Force 0.0.0.0 to ensure visibility inside the container
    host = os.getenv("HOST", "0.0.0.0")

    print(f"Starting FastAPI on {host}:{args.port}")

    uvicorn.run(
        "api.main:app",
        host=host,
        port=args.port,
        log_level="info",
        reload=reload_mode,
    )
